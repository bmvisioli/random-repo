package com.github.bmvisioli.lun.model;

public class Runway {

	private int id;
	private String surface;
	private int airport;
	private String leIdent;

	public int getId() {
		return id;
	}

	public String getSurface() {
		return surface;
	}

	public int getAirport() {
		return airport;
	}
	
	public String getLeIdent() {
		return leIdent;
	}

}
